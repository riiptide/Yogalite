    private static func flowSteps(_ holds: [FlowHold], transitionDuration: TimeInterval = 5) -> [PracticeStep] {
        guard let firstHold = holds.first else { return [] }

        var steps: [PracticeStep] = [
            hold(firstHold.title, firstHold.pose, duration: firstHold.duration, breath: firstHold.breath, firstHold.instruction, side: firstHold.side)
        ]
        var previousHold = firstHold

        for nextHold in holds.dropFirst() {
            steps.append(
                move(
                    "\(previousHold.pose.name) to \(nextHold.pose.name)",
                    previousHold.pose,
                    nextHold.pose,
                    duration: transitionDuration,
                    breath: nextHold.breath == .natural ? .natural : nextHold.breath,
                    transitionInstruction(from: previousHold.pose, to: nextHold.pose, side: nextHold.side),
                    side: previousHold.side,
                    endSide: nextHold.side
                )
            )
            steps.append(
                hold(nextHold.title, nextHold.pose, duration: nextHold.duration, breath: nextHold.breath, nextHold.instruction, side: nextHold.side)
            )
            previousHold = nextHold
        }

        return steps
    }

    private static func transitionInstruction(from start: Pose, to end: Pose, side: PracticeSide) -> String {
        if let sideName = side.displayName {
            return "Move with control into \(end.name) on the \(sideName.lowercased()) side."
        }
        return "Move with control from \(start.name) into \(end.name)."
    }

    private static func hold(_ pose: Pose, duration: TimeInterval, breath: BreathCue, _ instruction: String, side: PracticeSide = .none) -> PracticeStep {
        hold(pose.name, pose, duration: duration, breath: breath, instruction, side: side)
    }

    private static func hold(_ title: String, _ pose: Pose, duration: TimeInterval, breath: BreathCue, _ instruction: String, side: PracticeSide = .none) -> PracticeStep {
        PracticeStep(kind: .hold, title: title, startPose: pose, duration: duration, breathCue: breath, instruction: instruction, side: side)
    }

    private static func move(
        _ title: String,
        _ start: Pose,
        _ end: Pose,
        duration: TimeInterval,
        breath: BreathCue,
        _ instruction: String,
        side: PracticeSide = .none,
        endSide: PracticeSide = .none
    ) -> PracticeStep {
        PracticeStep(kind: .transition, title: title, startPose: start, endPose: end, duration: duration, breathCue: breath, instruction: instruction, side: side, endSide: endSide)
    }
